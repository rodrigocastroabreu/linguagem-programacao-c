document.addEventListener('DOMContentLoaded', function () {
  const fallbackImages = document.querySelectorAll('img[data-fallback-emoji]');
  fallbackImages.forEach(function (image) {
    image.addEventListener('error', function () {
      const wrapper = image.parentElement;
      if (!wrapper) return;
      wrapper.innerHTML = '<div class="w-full h-full flex items-center justify-center text-white text-4xl">' + image.dataset.fallbackEmoji + '</div>';
    });
  });
});
