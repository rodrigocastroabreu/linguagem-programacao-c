#include <stdio.h>

// Programa que trabalha com diferentes tipos de dados
int main() {
    int inteiro = 42;
    float decimal = 3.14f;
    char caractere = 'A';
    
    printf("Inteiro: %d\n", inteiro);
    printf("Decimal: %.2f\n", decimal);
    printf("Caractere: %c\n", caractere);
    
    return 0;
}
